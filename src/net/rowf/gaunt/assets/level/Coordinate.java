package net.rowf.gaunt.assets.level;

/**
 *
 * @author woeltjen
 */
public class Coordinate {
    private int x, y;
    
    public Coordinate (int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
